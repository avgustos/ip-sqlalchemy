DROP TABLE IF EXISTS tbl_clients;
CREATE TABLE IF NOT EXISTS tbl_clients(
	client_id INTEGER PRIMARY KEY AUTOINCREMENT,		-- Κωδικός Πελάτη (AUTO NUMBER)
	client_status INTEGER NOT NULL DEFAULT -1,			-- Status (1=ΕΝΕΡΓΟΣ, 2=ΜΗ ΕΝΕΡΓΟΣ, 3=ΥΠΟΨΗΦΙΟΣ)
	client_category INTEGER NOT NULL DEFAULT -1,		-- Κατηγορία (1=ΦΥΣΙΚΟ ΠΡΟΣΩΠΟ, 2=ΕΤΑΙΡΕΙΑ)
	client_first_name VARCHAR(255),						      -- Όνομα
	client_last_name VARCHAR(255) NOT NULL,				  -- Επώνυμο
	client_fathers_name VARCHAR(255),					      -- Πατρώνυμο
	client_sex INTEGER DEFAULT -1,						      -- Φύλο (1=ΑΝΔΡΑΣ, 2=ΓΥΝΑΙΚΑ)
	client_birthdate INTEGER,							          -- Ημ. Γέννησης
	client_birthplace VARCHAR(255),						      -- Τόπος Γέννησης
	client_nationality INTEGER DEFAULT -1,				  -- Υπηκοότητα (ΛΙΣΤΑ)
	client_marital_status INTEGER DEFAULT -1,			  -- Οικ. Κατάσταση (1=ΕΓΓΑΜΟΣ, 2=ΑΓΑΜΟΣ, 3=ΔΙΑΖΕΥΓΜΕΝΟΣ, 4=ΧΗΡΟΣ /ΧΗΡΑ)
	client_tin INTEGER,									            -- ΑΦΜ
	client_tax_office INTEGER DEFAULT -1,				    -- ΔΟΥ (ΛΙΣΤΑ)
	client_occupation INTEGER DEFAULT -1,				    -- Επάγγελμα (ΛΙΣΤΑ)
	client_occupation_details VARCHAR(255),				  -- Περιγραφή
	client_ssn INTEGER,									            -- ΑΜΚΑ
	client_ss_fund INTEGER DEFAULT -1,					    -- Ταμείο (ΛΙΣΤΑ)
	client_comments	VARCHAR(255),						        -- Σχόλια
	client_entry_date INTEGER DEFAULT (datetime('now','localtime')),	-- Ημερομηνία Εισαγωγής στη Βάση Δεδομένων (συνίσταται να είναι UTC)
	FOREIGN KEY(client_status) REFERENCES tlkp_client_status_ids(client_status_id),
	FOREIGN KEY(client_category) REFERENCES tlkp_client_category_ids(client_category_id),
	FOREIGN KEY(client_sex) REFERENCES tlkp_sex_ids(sex_id),
	FOREIGN KEY(client_nationality) REFERENCES tlkp_nationality_ids(nationality_id),
	FOREIGN KEY(client_marital_status) REFERENCES tlkp_marital_status_ids(marital_status_id),
	FOREIGN KEY(client_tax_office) REFERENCES tlkp_tax_office_ids(tax_office_id),
	FOREIGN KEY(client_occupation) REFERENCES tlkp_occupation_ids(occupation_id),
	FOREIGN KEY(client_ss_fund) REFERENCES tlkp_ss_fund_ids(ss_fund_id)
);

